1 ¿Qué es el Shell Scripting?

 Es un lenguaje de scripting que permite interpretar ciertos comandos
 con la shell del S.O relacionando comandos e instrucciones conocidas. 

 ¿A qué tipos de tareas están orientados los script?

  Shell scripts can be used by programmers to develop programs,
   scripts to perform system-level tasks,
   finding patterns in a file, processing the data in a file 
   based on the user inputs, etc. can be done using shell scripts

 ¿Los scripts deben compilarse? ¿Por qué?

  No, son ejecutados por un intérprete que lee el archivo de código fuente al momento

